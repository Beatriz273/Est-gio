# A estratégia seria a seguinte: como sabemos, ao ligar uma lâmpada e deixá-la acesa durante um tempo
# ela irá esquentar.
# Portanto, utilizaremos a seguinte estratégia:
# Ligaremos a lampada denominada como "A" e então, deixaremos ela por algum tempo ligada.
# Após desligar a lampada "A", ligaremos a lâmpada B.
# Ao entrar na primeira sala verificaremos se a lâmpada possui uma das três características possíveis:
# Ligada, quente ou fria.
# Caso esteja ligada, defineremos essa sala correspondente ao interruptor "B"
# Caso esteja fria, defineremos essa sala correspondente ao interruptor "C"
# Caso esteja quente, defineremos essa sala correspondente ao interruptor "A"
# A ida a próxima sala definirá as duas últimas salas, com base nas informações anteriores.